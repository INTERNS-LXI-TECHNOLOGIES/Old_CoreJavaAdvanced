package com.lxisoft.snakenladder.model;

public class Snake {

	int snakeHead,snakeTail;
	public Snake(int snakeHead, int snakeTail) {
		this.snakeHead=snakeHead;
		this.snakeTail=snakeTail;
		
	}
	public int getSnakeHead()
	{
		return snakeHead;
	}
	public int getSnakeTail()
	{
		return snakeTail;
	}
	

}
